<h3>Télécharger nos épisodes !</h3>
<p>Cher ami, tu te rends sur cette page parce que tu désires télécharger nos épisodes, n'est-ce pas ? Et tu as bien raison !<br />
Sur cette page, aucun lien de téléchargement n'est disponible, mais seulement des explications. Tout les liens de téléchargement se trouvent directement sur les pages des projets.</p>

<h2><img src="http://www.paranoland.com/emoticones-smileys/smileys/etonner/02.gif">Où c'est qu'on télécharge !?</h2>
<p>Directement sur la >> <a href="index.php?page=series" target="_blank">page projets</a> <<<br />
Clique sur la série que tu veux, puis sur l'épisode correspondant. Les détails de l'épisode aparaissent ainsi qu'une liste de lien.
</p>

<h2><img src="images/icones/ddl.png">Le Téléchargement Direct (ou DDL)</h2>
<p>C'est la meilleure solution ! Et les premiers liens dispos lors de la sortie d'un épisode. Il suffit de cliquer sur le lien et ça télécharge tisouite :)<br />
Certains navigateurs tels que Firefox ouvrent la vidéo plutôt que de la télécharger. La solution : Clique sur le bouton de droite de la souris et fais "Enregistrer la cible du lien sous..."</p>

<h2>Téléchargement <img src="images/icones/megaup.jpg"> Megaupload, <img src="http://zerofansub.net/images/icones/free.jpg"> Free, Zshare, ect.</h2>
<p>Nous mettons du temps à uploader les épisodes sur ces sites, et nous les déconseillons. Ils sont utiles pour les personnes ayant free ou un compte prenium Megaupload, ect.<br />
<b>Comment télécharger sur Megaupload ?</b>
Tape les 3 lettres demandées, clique sur Download. Patiente 45 secondes puis clique sur <img src="http://wwwstatic.megaupload.com/gui/b_free.gif">. Le nombre de téléchargement est très limité.
</p>

<h2><img src="images/icones/torrent.png"> Téléchargement en torrent</h2>
<p>Le torrent est du p2p. Nécéssite donc qu'il y est des personnes en sources pour télécharger. Merci de rester en source le maximum possible !<br /><b>Comment télécharger en torrent ?</b><br />
Il te faut un client tel que <a href="http://www.bittorrent.com/" target="_blank">Bittorrent</a>. Sur <a href="http://bt.fansub-irc.org:2005/index.html?filter=zero" target="_blank">le tracker</a>, clique sur le lien correspondant à l'épisode que tu désires télécharger. Ton client va s'ouvrir automatiquement. Si le torrent contient plusieurs fichiers, tu peux ne séléctionner que ceux qui t'intéressent. Une fois le téléchargement términé, reste en source ! C'est-à-dire, laisse ton client ouvert et ne déplace pas le fichier ^^ Merci d'avance.</p>

<h2><img src="images/icones/xdcc.png"> Téléchargement par XDCC</h2>
<p>Téléchargement plutôt rapide, mais nécéssite de posséder un client tel que <a href="http://mirc.zice.net/translations/french.html" target="_blank">Mirc</a>. Grâce à lui, tu peux aussi discuter avec les membres de la team ! Mais ça, c'est une autre histoire.<br />
<b>Comment télécharger en XDCC ?</b><br />
<a href="irc://irc.Fansub-IRC.eu/zero">Clique-ici</a>. Ce lien ouvre ton client irc ainsi que le channel de la team.<br />
Avant toute chose, configure-le de la manière suivante : <a href="http://xdcc.angel-sub.net/xdcc.htm" target="_blank">Lien !</a><br />
Double-clique sur [XDCC]`Rin ou [Zero]`Rin. Tu peux discuter, mais c'est un bot, donc il répondra pas :) Par contre, si tu lui dit <span style="color: #009900;">xdcc list</span> Il va t'envoyer la liste compléte des fichiers qu'il a en stock. Repère quel est le numéro du pack que tu veux télécharger, puis envoie au bot la commande <span style="color: #009900;">xdcc send #NUMERO</span> en remplacant par le numéro.
</p>

