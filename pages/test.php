<strong>5 bonnes raisons de passer de Windows &agrave; Ubuntu</strong><br /><br />
<div class="quote"><strong>Simplicité</strong><br />
Le plus important pour une distribution de Linux qui se veut «&nbsp;grand public » est avant tout d'avoir un premier aspect visuel soigné. C'est ce qu'à réussi Ubuntu avec GNOME (même si le choix du thème Human par défaut était peut-être contestable...). Il est vrai que l'installation peut paraître un peu vieillotte à première vue ce qui pourrait rebuter quelqu'un qui a déjà installé Linux avec un installateur graphique (ou Windows) mais elle est vraiment rapide et on n'installe qu'une fois (j'espère que les futures versions viendront tout de même combler ce manque).<br />
Le choix d'un environnement graphique simple et d'un nombre de logiciels installés limité rassure le débutant, le menu de GNOME est bien agencé et la description des programmes est claire. Une personne qui est passée de Windows à Ubuntu retrouve progressivement les logiciels libres qu'elle avait déjà commencée à utiliser, les incontournables sont au rendez-vous&nbsp;: Firefox, Thunderbird, The Gimp, OpenOffice, ...<br />
<br /><strong>Stabilité</strong><br />
C'est l'un des atout indéniable du monde de Linux. Finis les plantages à répétition, le système qui se fige sans savoir pourquoi ou le rapport de 50 pages qui se ferme brutalement. Bienvenue dans le monde du multi-tâches. Après quelques mois d'utilisation, et ce malgré une utilisation intensive, je n'ai pas réussi à planter le système. Au pire une application qui fige et que je dois «&nbsp;killer » en console mais sinon rien de plus méchant, à tel point qu'on en arrive à prendre de très mauvaises habitudes (sauvegarde de documents toutes les 10 pages, ...) qui pourraient s'avérer désastreuse en cas de futur travail sous Windows&nbsp;!<br />
De plus, il est très rare que le système «&nbsp;rame », Linux a vraiment une gestion de la mémoire qui est bien mieux optimisée que celle de Windows, je m'en rend compte chaque jour lorsque je compte le nombre d'applications ouvertes et ce même après plusieurs jours sans redémarrage.<br />
<br /><strong>Sécurité</strong><br />
Un ordinateur avec un WindowsXP fraîchement installé ne tient pas une heure connecté à Internet sans être infesté de troyans et autres joyeusetés qui font le quotidien des personnes sous Windows... Sous Linux oubliez tout ça, un bon firewall suffit&nbsp;!<br />
Les débutants ne seront pas désappointés par la gestion multi-utilisateurs de Linux puisque Ubuntu utilise une gestion «&nbsp;camouflée » du super-utilisateur root et un seul mot de passe est donc nécessaire en cas d'utilisation de la machine par une personne dans le cadre d'une utilisation Desktop. Est-ce vraiment sécurisé&nbsp;? Oui, le simple fait de donner le même mot de passe à l'utilisateur ayant installé le système et au super-utilisateur ne pose aucun problème, à moins peut-être de douter de soi ;-).<br />
<br /><strong>Liberté</strong><br />
Je trouve que les programmes sont beaucoup plus configurables et que les paramètres peuvent être appliqués à tout le système sans que ça pose de problème, par exemple agrandir la police de tout les logiciels car on dispose d'une grande résolution est vraiment appréciable.<br />
L'autre avantage d'un système libre est d'avoir la possibilité d'obtenir le code source de tous les programmes installés sur sa machine, ça permet entre autres d'adapter les fonctionnalités de n'importe quel programme mais bon là ça ne concerne que les geeks :-).<br />
<br /><strong>Légalité</strong><br />
C'est surprenant au début, oubliées les recherches de «&nbsp;cracks » ou «&nbsp;keygen ». Vous avez juste à ouvrir votre gestionnaire de programme et à chercher le programme qui répond à votre besoin. Ce gestionnaire le télécharge, l'installe et vérifie au passage s'il ne nécessite pas des dépendances. J'appelle ça de l'informatique moderne&nbsp;: simple, rapide et efficace.<br />
C'est peut-être un des tournant du monde du libre d'ailleurs. L'augmentation du prix des logiciels, la répression associée à la baisse des salaires sont probablement les meilleurs vecteurs d'introduction de Linux dans le foyer de «&nbsp;Monsieur-Tout-le-Monde », qui sait&nbsp;?<br />
<br /><br />
Vous l'aurez compris, l'utilisation d'Ubuntu au quotidien offre de nombreux avantages qui au-delà de l'idéologie du libre apportent un confort d'utilisation indéniable, aussi bien en terme d'efficacité qu'en terme de légalité. Je ne vous promet pas le bonheur absolu, ni la résolution de tous vos problèmes&nbsp;: Ubuntu n'est pas une secte&nbsp;! Mais si vous voulez (re)découvrir ce qu'est l'ergonomie en termes d'informatique ou si vous estimez qu'il est dangereux d'être tributaire des grands éditeurs de logiciels, le moment est venu d'essayer ce qui deviendra certainement votre nouvel environnement&nbsp;: <a href="http://www.ubuntu-fr.org">Ubuntu</a>.<br />
<br /><br />
<i>Source : <a href="http://www.biologeek.com/logiciels-libres,ubuntu/windows-ubuntu-raisons-succes/" target="_blank">biologeek.com</a></i>
</div>
<br /><br />
<b>Unbuntu pour l'utilisateur lamda ? C'est une blague ? Ce sont les gros geeks qui s'y connaissent, le gars moyen utilise windows.</b><br />
À bas les prégugés ! Ce n'est pas parce qu'Ubuntu est basé sur Linux que c'est forcément "trop compliqué avec pleins de 0 et de 1". 
Ubuntu est particulièrement adapté aux "débutants" et grâce à la documentation riche et l'énorme communauté derrière, impossible de ne pas trouver réponses à ses questions !
S'il y a des questions... Car je suis sûre que même sans la documentation, votre grand-mère saurait s'en servir.<br />
<br />
<b>Déjà, ça se vend où ce truc ?</b><br />
Ubuntu ne se vend pas... Et c'est bien là son avantage ! Tout est gratuit et surtout LIBRE !<br />
<br />
<b>Partout, dans tous les magasins, tu vois Windows comme système d'exploitation sur les pc. Donc, je ne pense pas que ce soit à la portée de tout le monde.</b><br />
Normal que Windows soit partout, c'est ça, la stratégie commerciale, l'achat forcé.<br />
<br />
<b>Est-ce qu'Ubuntu utilise une souris à deux clics ?</b><br />
C'est quoi cette question ? Evidemment qu'Ubuntu utilise 2 clics ! Ca ressemble vachement plus à Windows que ce qu'on pense...<br />
<br />
<b>Comment on installe l'OS sur CD ?</b><br />
Installer Ubuntu, ça se fait en 3 clics.<br />
- Installer OS sur CD (télécharger le contenu du CD sur le site officiel puis graver sur CD avec n'importe quel logiciel de gravure) ou Commander CD sur internet<br />
- Mettre CD dans ordinateur<br />
- Choisir langue<br />
- Faire joujou<br />
<br />
<b>Combien de place ça prend sur un pc ?</b><br />
Windows 7 demande environ 16 Go pour l'installation. Ubuntu, lui, n'en demande que 4.<br />
<br />
<b>Quand tu installes, tu peux jongler avec les deux systèmes ? Dans ce cas, tu achètes Windows 7 et tu installes Ubuntu ?</b><br />
A l'installation d'ubuntu, on te propose immédiatement si tu veux effacer tout ce que tu as ou si tu veux partager avec windows. Si tu choisis de partager, quand tu démarres ton PC, on te demandes si tu veux démarrer avec windows ou ubuntu. Ton windows est inchangé après l'installation.
Oui, tu peux acheter un PC avec windows 7 (ou en monter un) et y mettre Ubuntu aussi.<br />
<br />
<b>Est-ce que ça existe des pc avec Ubuntu déjà installé ?</b><br />
Ca existait du temps où les e-pc n'était pas assez puissants pour utiliser Windows, donc ils mettaient ubuntu par obligation. Encore une preuve de sa legéreté par rapport à son concurrent.<br />
<br />
<b>Si c'est pas le cas, t'es obligé d'acheter windows 7 ^^</b><br />
En général, quand tu achètes un pc tout fait, il y a Windows dessus, mais tu peux très bien demandé à ce qu'on te donne la machine sans OS. Faire cette demande n'a pas grand interêt, autant l'acheter et mettre soi-même Ubuntu à la place de Windows.
Si tu montes toi-même ton PC, alors il n'y aura pas d'OS et tu peux choisir Ubuntu.<br />
<br />
<b>Les programmes qu'on utilise sur Ubuntu et qu'on n'a pas su Windows, ce sont quels genres de programmes ? C'est pas des programmes pour des geek ?</b><br />
Non, non, non. Il existe un équivalent pour tout les programmes sur Ubuntu. Plus de détails sur cette page : <a href="http://doc.ubuntu-fr.org/logiciels" target="_blank">Logiciels Ubuntu</a>.<br />
