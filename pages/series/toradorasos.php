<h2>Co-production</h2>
<p style="text-align: center;"><a href="http://japanslash.free.fr" target="_blank"><img src="http://japanslash.free.fr/images/bannieres/naishi.png" border="0"></a><br />

Toradora! est le premier projet de Zéro réalisé en co-production.<br />
La team avec laquelle nous travaillons est la <a href="http://japanslash.free.fr" target="_blank">Maboroshi no fansub</a>.<br />
Actuellement composée de 8 membres avec Tohru pour présidente, cette team a fait ses débuts en Avril 2008. Elle a 11 projets en cours et 4 projets terminés.<br />Nous apprécions beaucoup cette team pour la qualité de leur releases et leur convivialité, et nous les remercions pour cette co-production qui se déroule en de très bons termes.</p>


