<b>Titre Original</b> Sketchbook - full color's<br />
<b>Site officiel</b> <a href="http://www.sketch-full.net/" target="_blank">Sketch-full.net</a><br />
<b>Année de production</b> 2007<br />
<b>Studio</b> <a href="http://www.hal-film.co.jp/" target="_blank">Hal film maker</a><br />
<b>Genre</b> Comédie<br />
<b>Auteur</b> Kobako Totan<br />
<b>Synopsis</b> Nous suivons la vie de Sora, une jeune adolescente très timide et qui, en raison de cela ne parle pas beaucoup. Donnant l’impression de vivre dans sa bulle, cette dernière a une passion pour le dessin. Ce penchant pour l’art l’entraîna à faire partie du club de dessin de son école où elle s’est fait plusieurs amies. Une des particularités de Sora est qu’elle ne quitte jamais son sketchbook afin de pouvoir retranscrire à n’importe quel moment sur papier, un évènement qui l’émerveille. Malheureusement, elle rencontre toujours le même problème, celui de ne jamais pouvoir terminer les dessins qu’elle fait sur des scènes éphémères (chat qui se lèche, feu d’artifice…). Légèrement déprimée à cause de cela, elle retrouve cependant très vite le sourire grâce à des petites choses qui paraissent insignifiantes, nous faisant ainsi partager son univers à la fois poétique et touchant.<br />
<b>Vosta</b> Spoonmoon
</p>

