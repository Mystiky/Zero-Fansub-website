<b>Année de production</b> 2009<br />
<b>Studio</b> <a href="http://www.genco.co.jp/" target="_blank">Genco</a><br />
<b>Genre</b> Comédie - Déjanté - Ecchi<br />
<b>Auteur</b> Endou Minari<br />
<b>Synopsis</b> Au milieu de l’année scolaire, Kanako est une adolescente qui décide de venir étudier dans un établissement pour filles : Ame no Kisaki. En faisant ceci, cette dernière espère avoir autant de chance en amour que ses parents qui s’y sont rencontrés (sa mère était une élève et son père un enseignant). Néanmoins, due à sa grande taille, Kanako n’a jamais pu imaginer entretenir une vraie relation avec un garçon et s’est finalement rendue compte qu’elle n’était attirée que par la gente féminine.
Alors qu’elle cherche son chemin, elle tombe sous le charme d’une étudiante accompagnée de sa servante et répondant au nom de Mariya. Après avoir fait connaissance, cette dernière lui indique le lieu où se situe le dortoir et lui donne rendez vous un peu plus tard dans la journée pour lui servir de guide à travers l’établissement. Cependant, le moment venu, Kanako découvre par hasard la vraie « nature » de Mariya qui, si celle-ci était découverte, suffirait à l’exclure de l’école. Afin que Kanako garde le secret, Mariya exploite vicieusement les penchants yuri que cette dernière tente de dissimuler et la force à rester près d’elle pour la surveiller. Ne pouvant faire face, Kanako lui obéit en devenant sa camarade de chambre et voit son espoir ardant de trouver son étoile promise s’amincir… <br />
<b>Vosta</b> GG
