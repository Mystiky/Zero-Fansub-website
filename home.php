<h2>Convergence lithosphérique et Océan Alpin</h2>
<h3>Introduction</h3>

<p><img src="images/main.jpg"><br><br>La surface terrestre est divisée en plaques lithosphériques. Celles-ci se déplacent en couvergeant et en divergeant. Notre étude géologique sur le terrain, dans les Alpes, nous a montré que le paysage était "transformé" au cours du temps. En effet, la courbure des roches, leur disposition en strates et la présence de fossiles nous a permis de faire ressortir deux idées :<br>
- Les Alpes ont subit une convergence ;<br>
- Ce fut, par le passé, un domaine océanique.</p>